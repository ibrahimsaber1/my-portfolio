// Helper functions
export const getCertificatesByOrganization = (organization) => {
  return certificatesData.filter(cert => cert.organization === organization);
};

export const getCertificatesBySkill = (skill) => {
  return certificatesData.filter(cert => 
    cert.skills.some(s => s.toLowerCase().includes(skill.toLowerCase()))
  );
};

export const getRecentCertificates = (limit = 5) => {
  return [...certificatesData]
    .sort((a, b) => new Date(b.issueDate) - new Date(a.issueDate))
    .slice(0, limit);
};